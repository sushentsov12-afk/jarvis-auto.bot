def _deg_distance(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """Евклидово расстояние в градусах (быстро, без тригонометрии)."""
    return math.sqrt((lat1 - lat2) ** 2 + (lon1 - lon2) ** 2)


def find_city(lat: float, lon: float) -> tuple[CityInfo | None, float]:
    """Возвращает (ближайший_город, расстояние_в_градусах)."""
    best: CityInfo | None = None
    best_dist = float("inf")
    for city in CITIES:
        d = _deg_distance(lat, lon, city.lat, city.lon)
        if d < best_dist:
            best_dist = d
            best = city
    return best, best_dist


def format_sos(lat: float | None = None, lon: float | None = None) -> str:
    """
    Формирует текст SOS.
    Если координаты переданы — показывает локальные номера города.
    Если нет — только федеральные.
    """
    lines: list[str] = ["🆘 <b>SOS — Авария / ДТП</b>\n"]

    # ── Экстренные (везде одинаковые) ──────────────────────────────────────
    lines += [
        "<b>🚨 Экстренные службы</b>",
        "🚔 ГИБДД — <code>102</code>",
        "🚑 Скорая — <code>103</code>",
        "🔥 МЧС/Спасатели — <code>112</code>",
        "",
    ]

    # ── Местные номера ──────────────────────────────────────────────────────
    if lat is not None and lon is not None:
        city, dist = find_city(lat, lon)

        if city and dist <= city.radius:
            lines.append(f"<b>📍 Ваш город: {city.name}</b>")
            lines.append(f"🚔 ГИБДД {city.name} — <code>{city.gai}</code>")
            lines.append("")
            lines.append(f"<b>⭐ Аварийные комиссары — {city.name}</b>")
            for name, phone in city.kommissar:
                lines.append(f"• {name}: <code>{phone}</code>")
        elif city:
            # Нашли ближайший, но далеко (трасса/село)
            lines.append(f"<b>📍 Ближайший крупный город: {city.name}</b>")
            lines.append(f"   (≈{dist * 111:.0f} км от вас)")
            lines.append(f"🚔 ГИБДД — <code>{city.gai}</code>")
            lines.append("")
            lines.append("<b>⭐ Аварийные комиссары</b>")
            for name, phone in city.kommissar[:2]:
                lines.append(f"• {name}: <code>{phone}</code>")
            lines.append("")
            lines.append("<b>🌐 Федеральные (бесплатно)</b>")
            for name, phone in FEDERAL_KOMMISSAR:
                lines.append(f"• {name}: <code>{phone}</code>")
        else:
            lines += _federal_block()
    else:
        # Геолокация не передана
        lines.append("📍 <i>Отправьте геолокацию — покажу местный номер ГИБДД</i>\n")
        lines += _federal_block()

    # ── Памятка ─────────────────────────────────────────────────────────────
    lines += [
        "",
        "<b>📋 Памятка при ДТП</b>",
        "1️⃣ Включить аварийку",
        "2️⃣ Знак: 15 м в городе / 30 м на трассе",
        "3️⃣ Позвонить 102 и комиссару",
        "4️⃣ Сфотографировать: место, машины, знаки",
        "5️⃣ Не трогать автомобили до ГИБДД",
    ]

    return "\n".join(lines)


def _federal_block() -> list[str]:
    out = ["<b>🌐 Федеральные комиссары (бесплатно, 24/7)</b>"]
    for name, phone in FEDERAL_KOMMISSAR:
        out.append(f"• {name}: <code>{phone}</code>")
    return out
