"""
dialog_engine.py — диалоговый движок диагностики.

Джек задаёт уточняющие вопросы (от простого к сложному),
и только после уточнения даёт точный ответ с причиной и ценой.

Архитектура:
  DialogTree  — дерево вопросов для одного симптома
  DialogNode  — один вопрос + варианты ответов → следующий узел или финальный диагноз
  DialogState — текущее состояние диалога конкретного пользователя
"""

from __future__ import annotations
from dataclasses import dataclass, field
from typing import Optional


# ─────────────────────────────────────────────────────────────────
# Структуры данных
# ─────────────────────────────────────────────────────────────────

@dataclass
class Diagnosis:
    """Финальный диагноз — простое решение всегда первым."""
    title: str
    explanation: str
    solution: str
    urgency: str          # critical / high / medium / low
    price: str


@dataclass
class DialogNode:
    """Один шаг диалога: вопрос + варианты ответов."""
    question: str
    options: list[str]                    # кнопки вариантов
    next_nodes: dict[str, str]            # ответ → id следующего узла
    diagnoses: dict[str, Diagnosis]       # ответ → финальный диагноз (если конечный)
    hint: str = ""                        # подсказка под вопросом


@dataclass
class DialogTree:
    """Дерево вопросов для одного симптома."""
    tree_id: str
    trigger_phrases: list[str]            # фразы-триггеры для запуска этого дерева
    intro: str                            # первое сообщение Джека
    root_node_id: str
    nodes: dict[str, DialogNode]


@dataclass
class DialogState:
    """Состояние диалога пользователя."""
    tree_id: str
    current_node_id: str
    answers: list[str] = field(default_factory=list)


