def find_tree(user_text: str) -> Optional[DialogTree]:
    """Ищет подходящее дерево диалога по тексту пользователя."""
    from rapidfuzz import fuzz
    text = user_text.lower().strip()
    best_tree: Optional[DialogTree] = None
    best_score = 0

    for tree in DIALOG_TREES:
        for phrase in tree.trigger_phrases:
            score = fuzz.token_set_ratio(text, phrase.lower())
            if score > best_score:
                best_score = score
                best_tree = tree

    return best_tree if best_score >= 55 else None


def get_node(tree: DialogTree, node_id: str) -> Optional[DialogNode]:
    return tree.nodes.get(node_id)


URGENCY_EMOJI = {"critical": "🚨", "high": "⚠️", "medium": "🔧", "low": "✅"}
URGENCY_LABEL = {
    "critical": "КРИТИЧНО — немедленно на СТО",
    "high": "Срочно — в ближайшие дни",
    "medium": "Плановый ремонт",
    "low": "Всё в порядке",
}


def format_diagnosis(d: Diagnosis) -> str:
    emoji = URGENCY_EMOJI.get(d.urgency, "🔧")
    label = URGENCY_LABEL.get(d.urgency, "")
    price_line = f"\n<b>💰 Стоимость:</b> {d.price}" if d.price else ""
    return (
        f"{emoji} <b>{d.title}</b>\n"
        f"<i>{label}</i>\n\n"
        f"<b>📋 Что происходит:</b>\n{d.explanation}\n\n"
        f"<b>✅ Что делать:</b>\n{d.solution}"
        f"{price_line}"
    )
